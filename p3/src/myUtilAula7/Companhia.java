package myUtilAula7;

public class Companhia {
    private String sigla, companhia;
    public Companhia(String sigla, String companhia) {
        this.sigla = sigla;
        this.companhia = companhia;
    }

    public String getSigla() {
        return sigla;
    }

    public String getCompanhia() {
        return companhia;
    }
}
